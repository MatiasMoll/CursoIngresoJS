function mostrar()
{
	var planeta;

	planeta = prompt("Ingrese un planeta");

	switch (planeta)
	{
		case "Mercurio":
			alert("Aca hace mucho calor");
			break;

		case "Venus":
			alert("Aca hace mucho calor");
			break;

		case "Tierra":
			alert("Aca vivimos");
			break;

		default:
			alert("Aca hace mas frio");		


	}
}
